package Linked_Lists;

import java.util.LinkedList;

public class Linked_List_Collections {
    public static void main(String[] args) {

        LinkedList<Integer> ll = new LinkedList<>();

        ll.addLast(2);
        ll.addLast(5);
        ll.addFirst(1);

        System.out.println(ll);
        
    }
    
}
